/**
 * Authentication Button Component
 * 
 * Displays Google sign-in button or user menu when authenticated.
 * Shows in top-right corner of main page.
 */

'use client';

import { useAuth } from '@/lib/auth-context';
import { useState } from 'react';

export default function AuthButton() {
  const { user, userProfile, signIn, signOut, loading } = useAuth();
  const [menuOpen, setMenuOpen] = useState(false);

  if (loading) {
    return (
      <div className="fixed top-4 right-4 z-50">
        <div className="w-10 h-10 rounded-full bg-neutral-800 animate-pulse" />
      </div>
    );
  }

  if (!user) {
    return (
      <div className="fixed top-4 right-4 z-50">
        <button
          onClick={signIn}
          className="flex items-center gap-2 px-4 py-2 bg-white text-black rounded-lg font-medium hover:bg-neutral-200 transition-colors"
        >
          <svg className="w-5 h-5" viewBox="0 0 24 24">
            <path
              fill="currentColor"
              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
            />
            <path
              fill="currentColor"
              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
            />
            <path
              fill="currentColor"
              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
            />
            <path
              fill="currentColor"
              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
            />
          </svg>
          Sign in
        </button>
      </div>
    );
  }

  return (
    <div className="fixed top-4 right-4 z-50">
      <div className="relative">
        <button
          onClick={() => setMenuOpen(!menuOpen)}
          className="flex items-center gap-2 px-4 py-2 bg-neutral-800 hover:bg-neutral-700 rounded-lg transition-colors"
        >
          {user.photoURL ? (
            <img
              src={user.photoURL}
              alt={user.displayName || 'User'}
              className="w-6 h-6 rounded-full"
            />
          ) : (
            <div className="w-6 h-6 rounded-full bg-neutral-600 flex items-center justify-center text-xs">
              {user.email?.[0].toUpperCase()}
            </div>
          )}
          <span className="text-sm font-medium text-white">
            {userProfile?.username || user.displayName || 'User'}
          </span>
          <svg
            className={`w-4 h-4 text-neutral-400 transition-transform ${
              menuOpen ? 'rotate-180' : ''
            }`}
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
          </svg>
        </button>

        {menuOpen && (
          <>
            {/* Backdrop */}
            <div className="fixed inset-0 z-40" onClick={() => setMenuOpen(false)} />

            {/* Menu */}
            <div className="absolute right-0 mt-2 w-56 bg-neutral-800 rounded-lg shadow-xl border border-neutral-700 overflow-hidden z-50">
              {userProfile?.username && (
                <a
                  href="/dashboard"
                  className="block px-4 py-3 hover:bg-neutral-700 transition-colors border-b border-neutral-700"
                >
                  <div className="text-sm font-medium text-white">Dashboard</div>
                  <div className="text-xs text-neutral-400 mt-0.5">
                    /api/{userProfile.username}
                  </div>
                </a>
              )}

              {!userProfile?.username && (
                <a
                  href="/dashboard"
                  className="block px-4 py-3 hover:bg-neutral-700 transition-colors border-b border-neutral-700"
                >
                  <div className="text-sm font-medium text-white">Complete Setup</div>
                  <div className="text-xs text-neutral-400 mt-0.5">
                    Choose your username
                  </div>
                </a>
              )}

              <button
                onClick={() => {
                  signOut();
                  setMenuOpen(false);
                }}
                className="w-full text-left px-4 py-3 hover:bg-neutral-700 transition-colors text-sm text-neutral-300"
              >
                Sign out
              </button>
            </div>
          </>
        )}
      </div>
    </div>
  );
}
